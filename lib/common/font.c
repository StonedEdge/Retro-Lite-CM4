//-------------------------------------------------------------------------

void
drawCharIndexed(
    int x,
    int y,
    uint8_t c,
    int8_t index,
    IMAGE_T *image)
{
    int j;
    for (j = 0 ; j < FONT_HEIGHT ; j++)
    {
        uint8_t byte = font[c][j];

        if (byte != 0)
        {
            int i;
            for (i = 0 ; i < FONT_WIDTH ; ++i)
            {
                if ((byte >> (FONT_WIDTH - i - 1)) & 1 )
                {
                    setPixelIndexed(image, x + i, y + j, index);
                }
            }
        }
    }
}

